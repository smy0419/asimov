// Implement for IAddress
// EncodeAddress returns the string encoding of a pay-to-pubkey-hash
// address.  Part of the Address interface.
func (a *Address) EncodeAddress() string {
	return hexutil.Encode(a[:])
}

// ScriptAddress returns the bytes to be included in a txout script to pay
// to a pubkey hash.  Part of the Address interface.
func (a *Address) ScriptAddress() []byte {
	return a[:]
}

func (a *Address) StandardAddress() [AddressLength]byte {
	return *a
}

func NewAddress(b []byte) (*Address, error) {
	// Check for a valid pubkey hash length.
	if len(b) != AddressLength {
		return nil, errors.New("address must be 21 bytes")
	}

	if b[0] != PubKeyHashAddrID && b[0] != ScriptHashAddrID && b[0] != ContractHashAddrID {
		return nil, errors.New("Invalid address netid")
	}
	addr := BytesToAddress(b)
	return &addr, nil
}

func NewAddressWithId(netId byte, b []byte) (*Address, error) {
	var a Address
	a[0] = netId

	if len(b) > AddressLength-1 {
		b = b[len(b)+1-AddressLength:]
	}
	copy(a[AddressLength-len(b):], b)
	return &a, nil
}

